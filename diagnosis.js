const searchInput = document.getElementById("searchInput");
const suggestionsList = document.getElementById("suggestions");

searchInput.addEventListener("input", function () {
    const query = searchInput.value.toLowerCase();
    const filteredDiagnosis = diagnosis.filter(
        diagnosis =>
            diagnosis.name.toLowerCase().includes(query) ||
            diagnosis.address.toLowerCase().includes(query)
    );

    suggestionsList.innerHTML = "";
    filteredDiagnosis.forEach(diagnosis => {
        const li = document.createElement("li");
        li.innerHTML = `<strong>Name:</strong> ${diagnosis.name}<br>
                        <strong>Address:</strong> ${diagnosis.address}<br>
                        
                        <strong>Phone:</strong> ${diagnosis.phone}<br>
                        <button onclick="window.open('${diagnosis.Website}', '_blank')">Website</button>
                        <button onclick="window.open('${diagnosis.mapLink}', '_blank')">Get Directions</button>`;

        li.addEventListener("click", () => {
            searchInput.value = diagnosis.name; // Fill the input with the clicked suggestion
            suggestionsList.innerHTML = ""; // Hide suggestions
        });

        suggestionsList.appendChild(li);
    });
});
